    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String feedback = "Update not successful";
        String title1 = titleTextField.getText();
        String author = authorTextField.getText();
        String publisher = publisherTextField.getText();
        String dop = dopTextField.getText();
        String edition = editionTextField.getText();
        String isbn = isbnTextField.getText();

        int updateQuery = 0;
        try {
            Connection con = null;
            //Load JBBC driver "com.mysql.jdbc.Driver"
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");

            PreparedStatement statement = con.prepareStatement("UPDATE library.books SET Title='" + title1 + "', Author='" + author + "', Publisher='" + publisher + "', publicationDate='" + dop + "', edition='" + edition + "', ISBNnumber='" + isbn + "' WHERE id ='" + storeid + "'");
            updateQuery = statement.executeUpdate();
            if (updateQuery != 0) {
                feedback = " Book details have been changed";
            } else {
                feedback = "submission unsuccessful 1";
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
            feedback = "submission unsuccessful 2";
        }
        fbLabel.setText(feedback);
    }//GEN-LAST:event_addButtonActionPerformed

