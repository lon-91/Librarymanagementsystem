    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String searchString = searchField.getText();
        String column = (String) modeComboBox.getSelectedItem();
        int count = 0;

        try {
            ResultSet result = null, countResult;
            Connection con = null;
            //Load JBBC driver "com.mysql.jdbc.Driver"
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            PreparedStatement countStatement = con.prepareStatement("SELECT COUNT(*) FROM library.books WHERE " + column + " like '%" + searchString + "%'");
            countResult = countStatement.executeQuery();
            while (countResult.next()) {
                count = Integer.parseInt(countResult.getString(1));
            }
            PreparedStatement statement = con.prepareStatement("SELECT * FROM library.books WHERE " + column + " like '%" + searchString + "%'");
            result = statement.executeQuery();
            array = new Object[count][8];
            int i = 0;
            while (result.next()) {
                array[i][0] = result.getString(1);
                array[i][1] = result.getString(2);
                array[i][2] = result.getString(3);
                array[i][3] = result.getString(4);
                array[i][4] = result.getString(5);
                array[i][5] = result.getString(6);
                array[i][6] = result.getString(7);
                if (result.getString(8).equals("1")) {
                    array[i][7] = "Available";
                } else {
                    array[i][7] = "Borrowed";
                }

                i += 1;
            }

            result.close();
            con.close();

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

        DefaultTableModel dtm = new DefaultTableModel(array, new String[]{"Book ID", "Title", "Author", "Publisher", "Publication Date", "Edition", "ISBN Number", "Status"});
        searchTable.setModel(dtm);
        searchTable.updateUI();

    }//GEN-LAST:event_searchButtonActionPerformed

